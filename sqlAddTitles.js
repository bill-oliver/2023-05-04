//
//  Callback for Database.each function
//
function ProcessReportTitles( err, row ){
	if( err ){
		throw err;
	}

	let sSQL = "INSERT INTO ReportTitles( ReportID, Title ) VALUES( ?, ? );"
	let i;
	for( i=0; i<sTitles.length; i++ ){
		if( sTitles[i].startsWith( row.AccessionNo ) ) {
			// console.log( row.AccessionNo, sTitles[i] );
			db.run( sSQL, [ row.ID, sTitles[i] ] );
			break;
		}
	}

	if( i >= sTitles.length )
		throw new Error( "No title for " + row.AccessionNo );
}
	
//
//  Main
//  ----
//
const sqlite3 = require('sqlite3').verbose();

let db = new sqlite3.Database('reports.sqlite')
let sql = "SELECT ID, AccessionNo FROM report";

try {
	db.each( sql, [], ProcessReportTitles );			// Process all items in db
}
catch( e ) {
	console.error( e.name );
	console.error( e.message );
}

db.close( );

